/**
 * Generates VAT entry with OSS support for EU transactions
 */
function generateVATEntry(row, index, customerCode, invoiceDate, taxAmount, netAmount, isCreditNote,
                         accounts, countryType, countryISO, settings, columnHeaders) {
    let xml = '        <VrsticaTemeljnice>\n';
    xml += `          <DatumKnjizbe>${invoiceDate}</DatumKnjizbe>\n`;
    xml += `          <OpisVrsticeTemeljnice>VAT - ${escapeXml(row['Number'] || '')}</OpisVrsticeTemeljnice>\n`;
    xml += `          <SifraKonta>${escapeXml(accounts.vat)}</SifraKonta>\n`;
    xml += '          <SifraDenarneEnote>EUR</SifraDenarneEnote>\n';
    xml += '          <ZnesekVBremeVDenarniEnoti>0.00</ZnesekVBremeVDenarniEnoti>\n';

    const vatAmount = isCreditNote ? taxAmount.toFixed(2) : Math.abs(taxAmount).toFixed(2);
    xml += `          <ZnesekVDobroVDenarniEnoti>${vatAmount}</ZnesekVDobroVDenarniEnoti>\n`;
    xml += '          <ZnesekVBremeVDomaciDenarniEnoti>0.00</ZnesekVBremeVDomaciDenarniEnoti>\n';
    xml += `          <ZnesekVDobroVDomaciDenarniEnoti>${vatAmount}</ZnesekVDobroVDomaciDenarniEnoti>\n`;

    // Add OSS fields for EU transactions with VAT
    if (countryType === 'eu' && settings.invoiceType === 'IR') {
        xml += generateOSSFields(row, index, netAmount, taxAmount, isCreditNote, countryISO, invoiceDate, settings, columnHeaders);
    } else {
        xml += '          <PredlozitevObracunaOSS>N</PredlozitevObracunaOSS>\n';
    }

    xml += '        </VrsticaTemeljnice>\n';

    return xml;
}

/**
 * Generates OSS (One Stop Shop) fields for EU VAT entries
 */
function generateOSSFields(row, index, netAmount, taxAmount, isCreditNote, countryISO, invoiceDate, settings, columnHeaders) {
    let xml = '          <PredlozitevObracunaOSS>D</PredlozitevObracunaOSS>\n';

    // Use ISO country code
    const code = countryISO && countryISO.length === 2 ? countryISO.toUpperCase() : 'EU';
    xml += `          <Drzava>${escapeXml(code)}</Drzava>\n`;

    if (code === 'EU') {
        console.error(`Row ${index + 1}: Could not find ISO code. Using EU as fallback.`);
    }

    // Calculate VAT percentage
    let vatPercent = netAmount !== 0 ? Math.abs((taxAmount / netAmount) * 100) : 0;

    // Try to get VAT rate from Excel headers first
    const dataVatRate = getVATRateFromData(row, columnHeaders);
    if (dataVatRate !== null) {
        vatPercent = dataVatRate;
    } else {
        // Round to standard EU VAT rate
        vatPercent = roundToStandardVATRate(vatPercent, countryISO);
    }

    xml += `          <Odstotek>${vatPercent.toFixed(2)}</Odstotek>\n`;

    // Set goods/services type
    const goodsServices = settings.businessType === 'services' ? 'Storitev' : 'Blago';
    xml += `          <BlagoStoritev>${goodsServices}</BlagoStoritev>\n`;
    xml += `          <DatumDDV>${invoiceDate}</DatumDDV>\n`;

    // Include both base amount fields for OSS
    const osnoveAmount = isCreditNote ? netAmount.toFixed(2) : Math.abs(netAmount).toFixed(2);
    xml += `          <ZnesekOsnoveVDomaciDenarniEnoti>${osnoveAmount}</ZnesekOsnoveVDomaciDenarniEnoti>\n`;
    xml += `          <ZnesekOsnoveVDenarniEnoti>${osnoveAmount}</ZnesekOsnoveVDenarniEnoti>\n`;

    return xml;
}

