// Helper functions

function getCountryISO(customerCountry) {
    if (!customerCountry) return null;

    if (customerCountry.length === 2) {
        return customerCountry.toUpperCase();
    }

    const countryKey = Object.keys(COUNTRY_CODES).find(
        key => key.toLowerCase() === customerCountry.toLowerCase()
    );

    return countryKey ? COUNTRY_CODES[countryKey] : null;
}

function validateVATRate(row, index, netAmount, taxAmount, countryISO, customerCountry) {
    if (countryISO && Math.abs(taxAmount) > 0.001 && netAmount !== 0) {
        const calculatedVatRate = Math.abs((taxAmount / netAmount) * 100);
        const expectedVatRate = EU_VAT_RATES[countryISO];

        if (expectedVatRate) {
            const rateDifference = Math.abs(calculatedVatRate - expectedVatRate);
            if (rateDifference > 0.5) {
                console.warn(`Row ${index + 1}: VAT rate mismatch for ${customerCountry} (${countryISO}). Expected ${expectedVatRate}%, calculated ${calculatedVatRate.toFixed(2)}%`);
            }
        }
    }
}

