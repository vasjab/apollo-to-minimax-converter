import { getCustomerCountry, classifyCountry, getCountryISOCode } from './CountryClassifier.js';
import { getAccountsByCountryType } from './AccountMapper.js';
import { generateCustomerCode } from './CustomerCodeGenerator.js';
import { getVATRateFromData, calculateVATRate, roundToStandardVATRate } from './VATDetector.js';
import { COUNTRY_CODES, EU_VAT_RATES, APP_VERSION } from '../config/constants.js';

/**
 * Generates Minimax XML from invoice data
 * Main entry point for XML generation
 *
 * @param {Array} data - Invoice data array
 * @param {Object} settings - Settings object
 * @param {Object} [columnHeaders={}] - Column headers for VAT rate detection
 * @returns {string} Generated XML string
 */
export function generateXML(data, settings, columnHeaders = {}) {
    let xml = generateXMLHeader();

    // Generate customer map
    const customerMap = generateCustomerMap(data, settings);

    // Generate customers section if enabled
    if (settings.includeCustomers && customerMap.size > 0) {
        xml += generateCustomersSection(customerMap);
    }

    // Generate invoices section
    xml += generateInvoicesSection(data, settings, customerMap, columnHeaders);

    // Close root element
    xml += '</miniMAXUvozKnjigovodstvo>';

    return xml;
}

/**
 * Generates XML header with version and schema information
 *
 * @returns {string} XML header
 */
function generateXMLHeader() {
    let xml = '<?xml version="1.0" encoding="UTF-8"?>\n';
    xml += `<!-- Generated by Minimax XML Converter v${APP_VERSION} - With Clearing Entries -->\n`;
    xml += '<miniMAXUvozKnjigovodstvo xmlns="https://moj.minimax.si/SI/CommonWeb/documents/schemas/miniMAXUvozKnjigovodstvo" ';
    xml += 'xmlns:xsi="http://www.w3.org/2001/XMLSchema-instance" ';
    xml += 'xsi:schemaLocation="https://moj.minimax.si/SI/CommonWeb/documents/schemas/miniMAXUvozKnjigovodstvo ';
    xml += 'https://moj.minimax.si/SI/CommonWeb/documents/schemas/miniMAXUvozKnjigovodstvo.xsd">\n';

    return xml;
}

/**
 * Generates customer map from invoice data
 * Extracts unique customers with their information
 *
 * @param {Array} data - Invoice data
 * @param {Object} settings - Settings object
 * @returns {Map} Map of customer names to customer objects
 */
function generateCustomerMap(data, settings) {
    const customerMap = new Map();

    if (!settings.includeCustomers) {
        return customerMap;
    }

    data.forEach((row, index) => {
        const customerName = row['Name'];
        if (customerName && !customerMap.has(customerName)) {
            const country = getCustomerCountry(row, settings.countryColumn);
            const customer = {
                name: customerName,
                address: row['Address'] || '',
                postalCode: row['Postal code'] || '',
                city: row['City'] || '',
                country: country,
                taxNumber: row['Tax number'] || ''
            };

            // Generate unique code for this customer
            const customerCode = generateCustomerCode(customer, customerMap.size, settings);
            customer.code = customerCode;

            customerMap.set(customerName, customer);
        }
    });

    return customerMap;
}

/**
 * Generates customers section XML
 *
 * @param {Map} customerMap - Map of customers
 * @returns {string} Customers section XML
 */
export function generateCustomersSection(customerMap) {
    let xml = '  <Stranke>\n';

    customerMap.forEach(customer => {
        xml += '    <Stranka>\n';
        xml += `      <Sifra>${escapeXml(customer.code)}</Sifra>\n`;
        xml += `      <Naziv>${escapeXml(customer.name)}</Naziv>\n`;

        // Following exact schema sequence order
        if (customer.address) {
            xml += `      <Naslov>${escapeXml(customer.address)}</Naslov>\n`;
        }

        if (customer.country) {
            // Convert country name to ISO code for better compatibility
            let countryCode = customer.country;

            // Check if it's already a 2-letter code
            if (countryCode.length !== 2) {
                countryCode = COUNTRY_CODES[customer.country] || customer.country;
            } else {
                countryCode = countryCode.toUpperCase();
            }

            xml += `      <KraticaDrzave>${escapeXml(countryCode)}</KraticaDrzave>\n`;
        }

        if (customer.postalCode) {
            xml += `      <PostnaStevilka>${escapeXml(customer.postalCode)}</PostnaStevilka>\n`;
        }

        if (customer.city) {
            xml += `      <NazivPoste>${escapeXml(customer.city)}</NazivPoste>\n`;
        }

        // Add DavcniZavezanec before IdentifikacijskaStevilka as per schema
        if (customer.taxNumber) {
            xml += `      <DavcniZavezanec>D</DavcniZavezanec>\n`;
            xml += `      <IdentifikacijskaStevilka>${escapeXml(customer.taxNumber)}</IdentifikacijskaStevilka>\n`;
        }

        xml += '    </Stranka>\n';
    });

    xml += '  </Stranke>\n';

    return xml;
}

/**
 * Generates invoices section XML
 *
 * @param {Array} data - Invoice data
 * @param {Object} settings - Settings object
 * @param {Map} customerMap - Customer map
 * @param {Object} columnHeaders - Column headers for VAT detection
 * @returns {string} Invoices section XML
 */
export function generateInvoicesSection(data, settings, customerMap, columnHeaders) {
    let xml = '  <Temeljnice>\n';

    data.forEach((row, index) => {
        xml += generateInvoiceEntry(row, index, settings, customerMap, columnHeaders);
    });

    xml += '  </Temeljnice>\n';

    return xml;
}

/**
 * Generates a single invoice entry with all sections
 *
 * @param {Object} row - Invoice row data
 * @param {number} index - Invoice index
 * @param {Object} settings - Settings object
 * @param {Map} customerMap - Customer map
 * @param {Object} columnHeaders - Column headers
 * @returns {string} Invoice entry XML
 */
export function generateInvoiceEntry(row, index, settings, customerMap, columnHeaders) {
    const netAmount = parseFloat(row['Total'] || 0);
    const grossAmount = parseFloat(row['Total w/ tax'] || netAmount);
    const taxAmount = grossAmount - netAmount;

    // Get customer code from map if customers are included
    let customerCode = '';
    if (settings.includeCustomers && row['Name'] && customerMap.has(row['Name'])) {
        customerCode = customerMap.get(row['Name']).code;
    }

    // Check if this is a credit note
    const isCreditNote = row['Type'] && row['Type'].toString().toLowerCase().includes('credit');
    const documentPrefix = isCreditNote ? 'Credit note' : 'Invoice';

    // Get customer country and determine accounts
    let customerCountry = getCustomerCountry(row, settings.countryColumn);
    let countryType = classifyCountry(customerCountry, settings.homeCountry);

    // Special handling: if no VAT and no country detected, assume third country
    if (!customerCountry && Math.abs(taxAmount) < 0.001) {
        countryType = 'third';
    }

    const accounts = getAccountsByCountryType(countryType, settings.businessType);

    // Get country ISO code with case-insensitive lookup
    let countryISO = getCountryISO(customerCountry);

    // Validate VAT rate matches expected rate for the country
    validateVATRate(row, index, netAmount, taxAmount, countryISO, customerCountry);

    // Debug for first record (uncomment for debugging)
    // if (index === 0) {
    //     logDebugInfo(row, customerCode, customerCountry, countryISO, countryType, isCreditNote, accounts, settings, taxAmount);
    // }

    let xml = '    <Temeljnica>\n';

    // Generate header
    xml += generateInvoiceHeader(row, index, settings, documentPrefix);

    // Generate DDV section (only for domestic transactions with VAT)
    if (countryType === 'domestic' && Math.abs(taxAmount) > 0.001 && settings.invoiceType === 'IR') {
        xml += generateDDVSection(row, index, customerCode, netAmount, taxAmount, isCreditNote, settings);
    }

    // Generate journal entries
    xml += generateJournalEntries(row, index, customerCode, netAmount, grossAmount, taxAmount,
                                  isCreditNote, documentPrefix, accounts, countryType, countryISO,
                                  settings, columnHeaders);

    xml += '    </Temeljnica>\n';

    return xml;
}

/**
 * Generates invoice header section
 *
 * @param {Object} row - Invoice row
 * @param {number} index - Invoice index
 * @param {Object} settings - Settings
 * @param {string} documentPrefix - Document prefix ('Invoice' or 'Credit note')
 * @returns {string} Invoice header XML
 */
export function generateInvoiceHeader(row, index, settings, documentPrefix) {
    let xml = '      <GlavaTemeljnice>\n';
    xml += `        <SifraVrsteTemeljnice>${escapeXml(settings.invoiceType)}</SifraVrsteTemeljnice>\n`;
    xml += `        <DatumTemeljnice>${formatDate(row['Date'], settings.dateFormat)}</DatumTemeljnice>\n`;
    xml += `        <OpisGlaveTemeljnice>${escapeXml(row['Number'] || documentPrefix + ' ' + (index + 1))}</OpisGlaveTemeljnice>\n`;
    xml += '      </GlavaTemeljnice>\n';

    return xml;
}

/**
 * Generates DDV (VAT book) section for domestic invoices
 *
 * @param {Object} row - Invoice row
 * @param {number} index - Invoice index
 * @param {string} customerCode - Customer code
 * @param {number} netAmount - Net amount
 * @param {number} taxAmount - Tax amount
 * @param {boolean} isCreditNote - Is credit note flag
 * @param {Object} settings - Settings
 * @returns {string} DDV section XML
 */
export function generateDDVSection(row, index, customerCode, netAmount, taxAmount, isCreditNote, settings) {
    let xml = '      <DDV>\n';
    xml += '        <DDVVrstica>\n';
    xml += '          <DDVGlava>\n';
    xml += `            <DatumDDV>${formatDate(row['Date'], settings.dateFormat)}</DatumDDV>\n`;
    xml += '            <KnjigaDDV>IR</KnjigaDDV>\n'; // IR = Knjiga izdanih računov
    xml += `            <DatumKnjizenjaDDV>${formatDate(row['Date'], settings.dateFormat)}</DatumKnjizenjaDDV>\n`;

    if (customerCode) {
        xml += `            <SifraStranke>${escapeXml(customerCode)}</SifraStranke>\n`;
    }

    xml += `            <Listina>${escapeXml(row['Number'] || 'IR:' + formatDate(row['Date'], settings.dateFormat) + '-' + (index + 1))}</Listina>\n`;
    xml += `            <DatumListine>${formatDate(row['Date'], settings.dateFormat)}</DatumListine>\n`;
    xml += '          </DDVGlava>\n';
    xml += '          <DDVStopnje>\n';
    xml += '            <DDVStopnja>\n';

    // Determine VAT rate code for DDV section
    let vatRateCode = settings.defaultVATRate;
    xml += `              <SifraStopnjeDDV>${vatRateCode}</SifraStopnjeDDV>\n`;

    // For services vs goods, use appropriate fields
    // For credit notes, keep negative values in DDV book
    const ddvOsnova = isCreditNote ? netAmount.toFixed(2) : Math.abs(netAmount).toFixed(2);
    const ddvAmount = isCreditNote ? taxAmount.toFixed(2) : Math.abs(taxAmount).toFixed(2);

    if (settings.businessType === 'services') {
        xml += `              <StoritevOsnova>${ddvOsnova}</StoritevOsnova>\n`;
        xml += `              <StoritevDDV>${ddvAmount}</StoritevDDV>\n`;
    } else {
        xml += `              <Osnova>${ddvOsnova}</Osnova>\n`;
        xml += `              <DDV>${ddvAmount}</DDV>\n`;
    }

    xml += '            </DDVStopnja>\n';
    xml += '          </DDVStopnje>\n';
    xml += '        </DDVVrstica>\n';
    xml += '      </DDV>\n';

    return xml;
}

/**
 * Generates journal entries section
 *
 * @param {Object} row - Invoice row
 * @param {number} index - Invoice index
 * @param {string} customerCode - Customer code
 * @param {number} netAmount - Net amount
 * @param {number} grossAmount - Gross amount
 * @param {number} taxAmount - Tax amount
 * @param {boolean} isCreditNote - Is credit note
 * @param {string} documentPrefix - Document prefix
 * @param {Object} accounts - Accounts object
 * @param {string} countryType - Country type (domestic/eu/third)
 * @param {string} countryISO - ISO country code
 * @param {Object} settings - Settings
 * @param {Object} columnHeaders - Column headers
 * @returns {string} Journal entries XML
 */
export function generateJournalEntries(row, index, customerCode, netAmount, grossAmount, taxAmount,
                                      isCreditNote, documentPrefix, accounts, countryType, countryISO,
                                      settings, columnHeaders) {
    let xml = '      <VrsticeTemeljnice>\n';

    const invoiceDate = formatDate(row['Date'], settings.dateFormat);
    const dueDate = formatDate(row['Date due'], settings.dateFormat);

    // 1. Receivables entry (debit)
    xml += generateReceivablesDebitEntry(row, customerCode, invoiceDate, dueDate, grossAmount, documentPrefix);

    // 2. Revenue entry (credit)
    xml += generateRevenueEntry(row, invoiceDate, netAmount, isCreditNote, accounts);

    // 3. VAT entry if applicable (credit)
    if (Math.abs(taxAmount) > 0.001 && accounts.vat) {
        xml += generateVATEntry(row, index, customerCode, invoiceDate, taxAmount, netAmount, isCreditNote,
                               accounts, countryType, countryISO, settings, columnHeaders);
    }

    // 4. Receivables credit entry (offsetting) - v2.21
    xml += generateReceivablesCreditEntry(row, customerCode, invoiceDate, grossAmount);

    // 5. Clearing account debit entry - v2.21
    xml += generateClearingEntry(row, customerCode, invoiceDate, grossAmount, settings);

    xml += '      </VrsticeTemeljnice>\n';

    return xml;
}

/**
 * Generates receivables debit entry
 */
function generateReceivablesDebitEntry(row, customerCode, invoiceDate, dueDate, grossAmount, documentPrefix) {
    let xml = '        <VrsticaTemeljnice>\n';
    xml += `          <DatumKnjizbe>${invoiceDate}</DatumKnjizbe>\n`;
    xml += `          <OpisVrsticeTemeljnice>${documentPrefix} ${escapeXml(row['Number'] || '')} - ${escapeXml(row['Name'] || '')}</OpisVrsticeTemeljnice>\n`;
    xml += `          <SifraKonta>${escapeXml(row._receivablesAccount || '1200')}</SifraKonta>\n`;

    if (customerCode) {
        xml += `          <SifraStranke>${escapeXml(customerCode)}</SifraStranke>\n`;
    }

    xml += `          <DatumZapadlosti>${dueDate}</DatumZapadlosti>\n`;
    xml += `          <DatumOpravljanja>${invoiceDate}</DatumOpravljanja>\n`;

    if (row['Reference']) {
        xml += `          <VezaZaPlacilo>${escapeXml(row['Reference'].substring(0, 30))}</VezaZaPlacilo>\n`;
    }

    xml += '          <SifraDenarneEnote>EUR</SifraDenarneEnote>\n';
    xml += `          <ZnesekVBremeVDenarniEnoti>${grossAmount.toFixed(2)}</ZnesekVBremeVDenarniEnoti>\n`;
    xml += '          <ZnesekVDobroVDenarniEnoti>0.00</ZnesekVDobroVDenarniEnoti>\n';
    xml += `          <ZnesekVBremeVDomaciDenarniEnoti>${grossAmount.toFixed(2)}</ZnesekVBremeVDomaciDenarniEnoti>\n`;
    xml += '          <ZnesekVDobroVDomaciDenarniEnoti>0.00</ZnesekVDobroVDomaciDenarniEnoti>\n';
    xml += '          <PredlozitevObracunaOSS>N</PredlozitevObracunaOSS>\n';
    xml += '        </VrsticaTemeljnice>\n';

    return xml;
}

